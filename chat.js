const TOPICS = ['Сон','Кормление','Плач','Развитие','Уход'];

function findAnswer(q) {
  const low = q.toLowerCase();
  let best = null, bestScore = 0;
  for (const item of FAQ) {
    let score = 0;
    for (const k of item.k) {
      if (low.includes(k)) score += k.length;
    }
    if (score > bestScore) { bestScore = score; best = item; }
  }
  if (best && bestScore > 0) return best.a;
  // Generic fallback
  return 'Не нашла точного ответа на ваш вопрос 🤔 Попробуйте переформулировать или выберите тему из кнопок выше. Если вопрос срочный — обратитесь к педиатру. <strong>Вы справляетесь! 💜</strong>';
}

function chatSend() {
  const inp = document.getElementById('chatInput');
  const q = inp.value.trim();
  if (!q) return;
  addMsg(q, 'user');
  inp.value = '';
  setTimeout(() => addMsg(findAnswer(q), 'bot'), 400);
}

function chatTopic(t) {
  const items = FAQ.filter(f => f.t === t);
  const random = items[Math.floor(Math.random() * items.length)];
  addMsg('Расскажи о теме: ' + t, 'user');
  setTimeout(() => addMsg(random.a, 'bot'), 300);
}

function addMsg(text, role) {
  const box = document.getElementById('chatMessages');
  const div = document.createElement('div');
  div.className = 'msg ' + role;
  div.innerHTML = text;
  box.appendChild(div);
  box.scrollTop = box.scrollHeight;
}

function chatKeydown(e) {
  if (e.key === 'Enter' && !e.shiftKey) { e.preventDefault(); chatSend(); }
}

let _chatInited = false;
function initChat() {
  if (_chatInited) return;
  _chatInited = true;
  addMsg('Привет! 👋 Я — консультант по режиму и уходу за малышом. Задайте любой вопрос или выберите тему ниже. <strong>Ответы основаны на рекомендациях ВОЗ, AAP и NHS.</strong>', 'bot');
}

let _topicsInited = false;
function initTopics(container) {
  if (_topicsInited) return;
  _topicsInited = true;
  TOPICS.forEach(t => {
    const b = document.createElement('button');
    b.className = 'chat-topic-btn'; b.textContent = t;
    b.onclick = () => { chatTopic(t); if(typeof hapticLight==='function') hapticLight(); };
    container.appendChild(b);
  });
}
